function getFilteredGuides() {
  const keyword = searchInput.value.trim().toLowerCase();

  return guides.filter((guide) => {
    const matchesCategory = activeCategory === "全部" || guide.category === activeCategory;
    const sourceText = [
      guide.game,
      guide.title,
      guide.category,
      guide.summary,
      guide.overview,
      guide.keyPoints.join(" "),
      guide.steps.join(" "),
      guide.suitableFor,
      guide.resourceTip,
      guide.warningTip,
      guide.officialGuide.join(" ")
    ].join(" ").toLowerCase();
    const matchesKeyword = !keyword || sourceText.includes(keyword);

    return matchesCategory && matchesKeyword;
  });
}

function renderPage() {
  renderFilters();
  renderCards(getFilteredGuides());
}

searchInput.addEventListener("input", () => {
  renderPage();
});

updateStats();
renderPage();
